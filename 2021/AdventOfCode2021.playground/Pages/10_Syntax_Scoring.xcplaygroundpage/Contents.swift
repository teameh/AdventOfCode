func parseInput(input: String) -> [[Character]] {
    input
        .split(separator: "\n")
        .map { $0.map { $0 } }
}

extension Character {
    private var opening: [Character] { ["(", "[", "{", "<"] }
    private var closing: [Character] { [")", "]", "}", ">"] }

    var opposite: Character? {
        opening.firstIndex(of: self).map { closing[$0] }
    }

    var isOpen: Bool {
        opening.contains(self)
    }
}

enum CorruptError: Error {
    case invalidChar(Character)
}

extension Array where Element == Character {
    func charsToCompleteLine() throws -> [Character] {
        try reduce(into: [Character]()) { openedChunks, char in
            if char.isOpen {
                openedChunks.append(char)
            } else if openedChunks.last?.opposite == char {
                _ = openedChunks.popLast()
            } else {
                throw CorruptError.invalidChar(char)
            }
        }
        .reversed()
        .compactMap(\.opposite)
    }
}

public func puzzle1(input: String) -> Int {
    let scores: [Character: Int] = [")": 3, "]": 57, "}": 1197, ">": 25137]
    let lines = parseInput(input: input)
    return lines
        .compactMap {
            do {
                _ = try $0.charsToCompleteLine()
                return nil
            } catch CorruptError.invalidChar(let char) {
                return scores[char]
            } catch {
                return nil
            }
        }
        .reduce(0, +)
}

public func puzzle2(input: String) -> Int {
    let scores: [Character: Int] = [")": 1, "]": 2, "}": 3, ">": 4]
    let lines = parseInput(input: input)
    let lineScores = lines
        .compactMap { line -> Int? in
            try? line
                .charsToCompleteLine()
                .reduce(0) { ($0 * 5) + (scores[$1] ?? 0) }
        }
        .sorted()
    return lineScores[lineScores.count / 2]
}

